#!/usr/bin/env bash
# ============================================================================
#  RiftStorm — merge partial sources into one shippable Carbon/Oxide plugin.
#
#  Reads every src/**/*.cs (partial class RiftStorm), de-dups using directives,
#  wraps the bodies in a single `namespace Oxide.Plugins { ... }`, and writes
#  dist/RiftStorm.cs.
#
#  NOTE: this is the conceptual contract (see docs/02-folder-structure.md). For
#  guaranteed brace-balanced output, pin a Roslyn-based merger in CI. The source
#  files in src/ are added incrementally as you follow docs/06..16.
#  Usage:  bash build/merge.sh
# ============================================================================
set -euo pipefail

HERE="$(cd "$(dirname "$0")" && pwd)"
SRC="$HERE/../src"
OUT="$HERE/../dist/RiftStorm.cs"

if [ ! -d "$SRC" ] || [ -z "$(find "$SRC" -name '*.cs' 2>/dev/null)" ]; then
  echo "No source files in $SRC yet — add classes per docs/06..16 first." >&2
  exit 1
fi

USINGS="$(grep -hR '^using ' "$SRC" | sort -u)"

{
  echo "// AUTO-GENERATED by build/merge.sh — do not edit. Edit src/ instead."
  echo "$USINGS"
  echo "namespace Oxide.Plugins"
  echo "{"
  for f in $(find "$SRC" -name '*.cs' | sort); do
    # strip per-file usings + the namespace wrapper; keep the class body lines
    sed -e '/^using /d' \
        -e '/^namespace Oxide.Plugins$/d' \
        "$f"
  done
  echo "}"
} > "$OUT"

echo "Wrote $OUT"
